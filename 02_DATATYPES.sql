# DATATYPES -> Datatypes ki madad se hum basically system ko ye batae hai ki table ke kis column me kis tarah ki information store hogi

-- 1) INT-> iska matlab hai ki hum integer value store kr rhe hai ( 4 Bytes)
-- 2) BIGINT-> ye bahut bade numbers ko store krta hai ( 8 Bytes)
-- 3) TINYINT-> ye small integers ko aapas me store krta hai (1 bytes)
-- 4) FLOAT-> Ye decimal numbers ko store krta hai
-- 5) DECIMAL-> ye fixed decimnal values ko store krta hai i.e decimal se pahele 5 digit and decimal ke baad 2 digit bss 
-- 6) CHAR-> char ka matlab hota hai character value let say humne likha CHAR(50) and iske andar humne likha ram to isne bss 3 space hi bhara 50 me se and baaki ki 47 spaces waste ho gyi 
-- 7) VARCHAR-> Varchar ka matlab hota hai variable character i.e let say humne likha ki varchar(50) and iske andar humne llikha ram to ye sirf 3 spaces ki jagah lega and baaki 47 ko free kar dega jisse ki memory save hogi yaani spaces waste nhi honge so hum mostly yhi use krte hai 
-- 8) DATE-> YYYY-MM-DD
-- 9) Boolean -> ye values ko 0/1 me store krta hai 0 means false and 1 means True 
